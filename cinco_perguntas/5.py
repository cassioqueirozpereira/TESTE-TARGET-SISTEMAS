# essa questão me fez lembrar de um parente onde na época as lâmpadas eram incandecentes e esquentavam, assim se podia resolver o problema com somente uma ida a sala das lâmpadas, porém como hj tudo é led, ja desenvolveram um problema com duas idas. Bom vamos a solução.

# primeiro:
# Ligue o primeiro interruptor e o deixe ligado.
# Deixe os outros dois desligados.
# segundo:
# Vá até a sala das lâmpadas pela primeira vez.
# A lâmpada que estiver acesa está ligada ao primeiro interruptor (o que você deixou ligado).
# As outras duas lâmpadas estarão apagadas.
# terceiro:
# Volte para a sala dos interruptores.
# Desligue o primeiro interruptor e ligue o segundo.
# quarto:
# Agora vá até a sala das lâmpadas pela segunda vez.
# A lâmpada que estiver acesa agora estará conectada ao segundo interruptor (que você acabou de ligar).
# A lâmpada que ainda está apagada e não acendeu em nenhum momento está conectada ao terceiro interruptor (que nunca foi ligado).